#include "libft.h"

void	ft_swap_int(int *a, int *b)
{
	int x;

	x = *a;
	*a = *b;
	*b = x;
}

void	ft_swap_char(char *a, char *b)
{
	char x;

	x = *a;
	*a = *b;
	*b = x;
}
